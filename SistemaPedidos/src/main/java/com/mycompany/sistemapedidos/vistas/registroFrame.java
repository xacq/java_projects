    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String apellido = jTextField2.getText();
        String telefono = jTextField3.getText();
        String mail = jTextField4.getText();
        String usuario = jTextField5.getText();
        String contrasena = jTextField6.getText();

        PersonaDAO persona = new PersonaDAO();
        if (persona.insertPersona(nombre, apellido, telefono, mail)) { // Crea la persona
            int personaId = persona.getPersonaId(nombre, apellido, mail); // Obtiene el id de la persona creada

            ClienteDAO cliente = new ClienteDAO();
            if (cliente.insertCli(personaId, usuario, contrasena)) { // Inserta el cliente
                JOptionPane.showMessageDialog(this, "Usuario registrado correctamente", "Registro Existoso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar el cliente", "Error en el registro", JOptionPane.ERROR_MESSAGE);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no pudo registrarse", "Error en el registro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

