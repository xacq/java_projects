package com.mycompany.rectanguloproject;

// Clase Punto para representar puntos en el plano
class Punto {

    private double x;
    private double y;

    public Punto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void desplazar(double dx, double dy) {
        this.x += dx;
        this.y += dy;
    }
}