    private void cargarPersonasEnTextFields() {
        // Asignar informaciÃ³n de personas a los JTextField
        if (!personas.isEmpty()) {
            Persona p1 = personas.get(0);
            jTextField1.setText(p1.getCodigo());
            jTextField2.setText(p1.getNombre());
            if (p1.isSuscrito()){
                jTextField3.setText("si");
            }else {
                jTextField3.setText("no");
            }
        }
        
        if (personas.size() > 1) {
            Persona p2 = personas.get(1);
            jTextField4.setText(p2.getCodigo());
            jTextField5.setText(p2.getNombre());
            if (p2.isSuscrito()){
                jTextField6.setText("si");
            }else {
                jTextField6.setText("no");
            }
        }
        
        if (personas.size() > 2) {
            Persona p3 = personas.get(2);
            jTextField7.setText(p3.getCodigo());
            jTextField8.setText(p3.getNombre());
            if (p3.isSuscrito()){
                jTextField9.setText("si");
            }else {
                jTextField9.setText("no");
            }
        }

    }
    
