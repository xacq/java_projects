    private void mostrarNotificaciones() {
        jTextArea1.setText(""); // Limpiar Ã¡rea de texto
        for (Persona persona : personas) {
            if (persona.isSuscrito()) {
                String mensaje = "NotificaciÃ³n para " + persona.getNombre() + 
                        ": Temperatura actual es " + this.sistemaMeteorologico.categorizarTemperatura() + 
                        " (" + (this.sistemaMeteorologico.getTemperatura()) + " C.)";
                jTextArea1.append(mensaje + "\n");

            }
            else{
                String mensaje = "No hay notificaciÃ³n para " + persona.getNombre();
                jTextArea1.append(mensaje + "\n");
            }
        }
    }


