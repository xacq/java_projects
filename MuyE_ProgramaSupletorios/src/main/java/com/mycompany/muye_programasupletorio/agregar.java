    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String apellido = jTextField2.getText();
        String cedula = jTextField3.getText();
        String apellido_hijo= (String)jComboBox1.getSelectedItem();
        padreDAO papa = new padreDAO();
        int cantidad_hijos = papa.contarHijos(cedula);
        int cantidad = 0;
        if (apellido == apellido_hijo) {
            cantidad = cantidad_hijos + 1 ;
            papa.insertarPadre(nombre, apellido, cedula, cantidad);
            JOptionPane.showMessageDialog(this, "Registro Generado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            cantidad = cantidad_hijos;
            papa.insertarPadre(nombre, apellido, cedula, cantidad);
            JOptionPane.showMessageDialog(this, "Registro Generado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

