    private void loadUserData() {
        try (Connection conn = DatabaseConnection.getConnection()) {
            String sql = "SELECT nombre, direccion, telefono, contrasena FROM cliente WHERE nombre = ? AND contrasena = ?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, username);
            statement.setString(2, password);
            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                jTextField1.setText(resultSet.getString("nombre"));
                jTextArea1.setText(resultSet.getString("direccion"));
                jTextField2.setText(resultSet.getString("telefono"));
                jTextField4.setText(resultSet.getString("contrasena"));
            } else {
                System.out.println("Usuario no encontrado.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }


