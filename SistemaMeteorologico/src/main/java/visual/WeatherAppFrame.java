    public WeatherAppFrame(WeatherData weatherData) {
        initComponents();
        this.weatherData = weatherData;
        this.weatherData.registerObserver(this);
        
        // Crea un array de personas para suscribirse/desuscribirse
        persons = new Person[]{
            new Person("Usuario1", weatherData),
            new Person("Usuario2", weatherData),
            new Person("Usuario3", weatherData),
            new Person("Usaurio4", weatherData),
            new Person("Usuario5", weatherData)
        };

        // Añade ActionListeners a los checkboxes
        addCheckBoxListeners();
        
        // Añade un ChangeListener al JSlider
        jSlider1.addChangeListener((ChangeEvent e) -> {
            int value = jSlider1.getValue();
            updateGraphicalRepresentation(value);
        });
    }
    
        // Método para añadir ActionListeners a los checkboxes
    private void addCheckBoxListeners() {
        JCheckBox[] checkBoxes = {jCheckBox1, jCheckBox2, jCheckBox3, jCheckBox4, jCheckBox5};
        for (int i = 0; i < checkBoxes.length; i++) {
            int index = i;
            checkBoxes[i].addActionListener((ActionEvent e) -> {
                if (checkBoxes[index].isSelected()) {
                    persons[index].subscribe();
                } else {
                    persons[index].unsubscribe();
                }
            });
        }
    }
    
    // Implementa el método update de la interfaz Observer
    @Override
    public void update(float temperature) {
        jSlider1.setValue((int) temperature);
        updateGraphicalRepresentation((int) temperature);
    }
    
        // Método para actualizar la representación gráfica en función del valor del slider
    private void updateGraphicalRepresentation(int temperature) {
        // Aquí puedes añadir lógica para actualizar el gráfico según la temperatura
        // Por ejemplo, cambiar el color de los paneles
        if (temperature <= 50) {
            jPanel1.setBackground(new java.awt.Color(102, 102, 255)); // Azul para frío
            jLabel6.setIcon(new ImageIcon("C:\\SistemaMeteorologico\\src\\main\\java\\images\\term1.jpg"));
        } else if (temperature <= 80) {
            jPanel1.setBackground(new java.awt.Color(255, 195, 36)); // Verde para templado
            jLabel6.setIcon(new ImageIcon("C:\\SistemaMeteorologico\\src\\main\\java\\images\\term2.jpg"));
        } else {
            jPanel1.setBackground(new java.awt.Color(255, 102, 102)); // Rojo para caliente
            jLabel6.setIcon(new ImageIcon("C:\\SistemaMeteorologico\\src\\main\\java\\images\\term3.jpg"));
        }
        
            // Mostrar mensaje por cada usuario suscrito si la temperatura supera los 50 o los 80 grados
        for (Person person : persons) {
            if (temperature > 50) {
                if (person.isIsSubscribed()){
                    JOptionPane.showMessageDialog(this, "La temperatura ha superado los 50 grados para " + person.getName());
                }                
            }
            if (temperature > 80) {
                if (person.isIsSubscribed()){
                    JOptionPane.showMessageDialog(this, "La temperatura ha superado los 80 grados para " + person.getName());
                }    
            }
        }
    }
    
