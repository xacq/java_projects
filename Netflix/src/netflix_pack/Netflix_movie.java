    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ("".equals(jTextField1.getText()) || "".equals(jTextField2.getText())||"".equals(jTextField3.getText()) || "".equals(jTextArea1.getText())){
            JOptionPane.showMessageDialog(null, "No ha llenado todos los campos");
        }
    	else {
    		Netflix_coneccion conectando = new Netflix_coneccion();
                Statement sentence=null;
    		Connection cxion = conectando.getConectar();
    	 	try {
	    		int gene = jComboBox1.getSelectedIndex();
                        gene+=1;
                        String name = jTextField1.getText();
                        String resu = jTextArea1.getText();
                        int fecha = Integer.valueOf(jTextField2.getText());
                        int duracion = Integer.valueOf(jTextField3.getText());
                         
                        String query = "INSERT INTO movie "
                                + "(id_genre,title,summary,"
                                + "year,duration) "
                                + "values('"+gene+"','"+name+"','"+resu+"','"+fecha+"','"+duracion+"')";
	    		sentence = cxion.createStatement();
	    		sentence.executeUpdate(query);
	    		JOptionPane.showMessageDialog(null, "Pelicula  agregada");
	    	} catch (HeadlessException | SQLException e){
	       		JOptionPane.showMessageDialog(null, "Error en ingreso de peliculas" + e);
	       	} finally {
	       		try {
	       			if (sentence != null) sentence.close();
	       			if (cxion != null) cxion.close();
	       		}catch (SQLException e) {
	       			JOptionPane.showMessageDialog(null, "Error en ingreso de peliculas" + e);
	       		}
	       	}
    	}
        
    }//GEN-LAST:event_jButton1ActionPerformed

