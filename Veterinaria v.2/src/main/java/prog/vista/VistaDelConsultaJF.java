    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Name = (String) jComboBox1.getSelectedItem();
        System.out.println(Name);
        for (Consulta obj : consultas){
            if (obj.getCodigo() == null ? Name == null : obj.getCodigo().equals(Name)){
                int respuesta = JOptionPane.showOptionDialog(
                    null,
                    "¿Deseas Eliminar este item?",
                    "Confirmación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    new Object[]{"Sí", "No"},
                    "Sí"
                );
                switch (respuesta) {
                    case JOptionPane.YES_OPTION -> {
                        consultas.remove(obj);
                        JOptionPane.showMessageDialog(null, "Item Eliminado.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        dispose();
                    }
                    case JOptionPane.NO_OPTION -> {
                        JOptionPane.showMessageDialog(null, "Item No eliminado.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        dispose();}
                    default -> System.out.println("Has cerrado el cuadro de diálogo.");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

