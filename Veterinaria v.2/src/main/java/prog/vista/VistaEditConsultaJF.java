    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Code = (String) jComboBox1.getSelectedItem();
        System.out.println(Code);
        int posicion;
        for (Consulta obj : consultas){
            if (obj.getCodigo().equals(Code)){
                jTextField1.setText(obj.getCodigo());
                jTextField2.setText(obj.getCodigoDoctor());
                jTextField3.setText(obj.getCodigoCliente());
                jTextField4.setText(obj.getCodigoMascota());
                jTextArea1.setText(obj.getDiagnostico());
                jTextField6.setText(obj.getFechaDeConsulta());
            }
        }
        jButton2.setEnabled(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (jTextField1.getText().isEmpty() || jTextArea1.getText().isEmpty() || jTextField6.getText().isEmpty() ){
                JOptionPane.showMessageDialog(null, "Faltan datos por ingresar", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                for (Consulta obj : consultas) {
                    if (obj.getCodigo().equals(jTextField1.getText())) {
                        obj.setCodigoDoctor(jTextField2.getText());
                        obj.setCodigoCliente(jTextField3.getText());
                        obj.setCodigoMascota(jTextField4.getText());
                        obj.setDiagnostico(jTextArea1.getText());
                        obj.setFechaDeConsulta(jTextField6.getText());
                    }
                }
                JOptionPane.showMessageDialog(null,"CONSULTA ACTUALIZADA","Mensaje", JOptionPane.INFORMATION_MESSAGE);                     
                dispose();
            }
        } catch (HeadlessException error) {
            JOptionPane.showMessageDialog(null, "Error: " + error.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

