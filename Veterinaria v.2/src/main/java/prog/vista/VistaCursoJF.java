    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField5.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Faltan datos por ingresar", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                ControladorCurso curso = new ControladorCurso();
                curso.Controlador(jTextField1.getText(), jComboBox1.getSelectedItem().toString(), jTextField2.getText(), jTextField3.getText(), jTextField5.getText(), jTextArea1.getText(), this.cursos);
                dispose();
            }
        } catch (HeadlessException error) {
            JOptionPane.showMessageDialog(null, "Error: " + error.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

