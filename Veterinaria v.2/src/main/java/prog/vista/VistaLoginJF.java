    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ControladorLeerArchivo<User> userFileReader = new ControladorLeerArchivo<>("login.txt");
        ArrayList<User> usuario = userFileReader.leerObjetos();
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Faltan datos por ingresar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        else{
            String nombre_user = jTextField1.getText();
            String pass_user = jTextField2.getText();
            for (User obj : usuario){
                if (obj.getCodigo().equals(nombre_user) && obj.getNombre().equals(pass_user)){
                    dispose(); // Cerrar el JFrame
                    new ControladorMenu().setVisible(true);
                    break;
                }
                else{
                    JOptionPane.showMessageDialog(null, "Error en los datos", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            
            
        }

    }//GEN-LAST:event_jButton1ActionPerformed



